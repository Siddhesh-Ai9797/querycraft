import re
from src.evaluation.sql_executor import normalize_sql, execute_sql


# Load BLEU metric from HuggingFace evaluate library
# This downloads a small metric file on first call


def compute_exact_match(predicted: str, gold: str) -> bool:
    """
    Check if predicted SQL exactly matches gold SQL after normalization.

    Args:
        predicted: SQL string generated by the model
        gold:      Ground truth SQL from the dataset

    Returns:
        True if they match after normalization, False otherwise
    """
    return normalize_sql(predicted) == normalize_sql(gold)


def compute_bleu(predicted: str, gold: str) -> float:
    """
    Compute BLEU score between predicted and gold SQL.

    Score range: 0 to 100. Higher is better.
    """
    from evaluate import load as hf_load
    bleu_metric = hf_load("sacrebleu")

    result = bleu_metric.compute(
        predictions=[predicted],
        references=[[gold]],
    )
    return round(result["score"], 2)


def compute_execution_accuracy(predicted: str, schema: str) -> bool:
    """
    Check if the predicted SQL executes without error.

    Args:
        predicted: SQL string generated by the model
        schema:    CREATE TABLE context from the dataset

    Returns:
        True if SQL executes successfully, False otherwise
    """
    result = execute_sql(predicted, schema)
    return result["success"]


def evaluate_single(
    predicted: str,
    gold: str,
    schema: str,
) -> dict:
    """
    Compute all three metrics for one example.

    Args:
        predicted: SQL generated by model
        gold:      Ground truth SQL
        schema:    CREATE TABLE context

    Returns:
        dict with exact_match, bleu, execution_accuracy
    """
    return {
        "exact_match":        compute_exact_match(predicted, gold),
        "bleu":               compute_bleu(predicted, gold),
        "execution_accuracy": compute_execution_accuracy(predicted, schema),
    }


def aggregate_metrics(results: list[dict]) -> dict:
    """
    Aggregate per-example metrics into dataset-level summary.

    Args:
        results: list of dicts from evaluate_single

    Returns:
        dict with mean values for all metrics
    """
    n = len(results)
    return {
        "exact_match_pct":        round(
            sum(r["exact_match"] for r in results) / n * 100, 2
        ),
        "mean_bleu":              round(
            sum(r["bleu"] for r in results) / n, 2
        ),
        "execution_accuracy_pct": round(
            sum(r["execution_accuracy"] for r in results) / n * 100, 2
        ),
        "n_examples":             n,
    }